                <h4><b>
                    3. Uso y finalidad del Tratamiento
                    </b></h4>
                <p>
                    El Comité “Salvemos a Rionegro”; reconoce que el Titular de los datos personales tiene derecho a
                    contar con una expectativa razonable de su privacidad, teniendo en todo caso en cuenta sus
                    responsabilidades, derechos y obligaciones con el Comité.
                </p>

                <p>
                    En virtud de la relación que se establezca entre usted y el Comité, éste recolecta, almacena, usa y
                    transfiere datos personales, a una entidad con autonomía administrativa, contractual y presupuestal,
                    organizada de manera desconcentrada, que tiene a su cargo el registro de la vida civil e
                    identificación
                    de los colombianos y la realización de los procesos electorales y los mecanismos de participación
                    ciudadana, como es la Registraduría Nacional del Estado Civil con plenas garantías para los
                    colombianos;
                    localizada dentro de Colombia. Los datos personales son utilizados para:
                </p>

                <p>
                    • Envío de información a entidades gubernamentales o judiciales por solicitud expresa de la misma.
                </p>
                <p>
                    • Soporte en procesos de auditoría externa/interna.
                </p>
                <p>
                    • Registro de la información de los Apoyadores con sus firmas en la base de datos del Comité.
                </p>
                <p>
                    • Recolección de datos para el cumplimiento de los deberes que como responsable de la información y
                    datos personales, le corresponde al Comité.
                </p>
                <p>
                    • Con propósitos de seguridad o prevención de fraude.
                </p>

                <p>
                    Si usted nos proporciona Datos Personales, ésta información será utilizada sólo para los propósitos
                    aquí
                    señalados, y no procederemos a vender, licenciar, transmitir o divulgar la misma fuera del Comité
                    “Salvemos a Rionegro”.
                </p>

                <p>
                    A fin de poner en práctica los propósitos descritos anteriormente, sus datos personales podrán ser
                    divulgados con los fines dispuestos anteriormente y explícitamente a la Registraduría Nacional del
                    Estado Civil según corresponde.
                </p>

