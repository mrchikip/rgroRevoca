                <h4><b>
                    4. Derechos del Titular
                    </b></h4>
                <p>
                    De conformidad con el artículo 8 de la Ley 1581 de 2012, los derechos que como titular le asisten en
                    relación con sus datos personales son:
                </p>

                <p>
                    a. Conocer, actualizar y rectificar sus datos personales frente al Comité como los Responsables del
                    Tratamiento o Encargados del Tratamiento. Este derecho se podrá ejercer, entre otros frente a datos
                    parciales, inexactos, incompletos, fraccionados, que induzcan a error, o aquellos cuyo Tratamiento
                    esté expresamente prohibido o no haya sido autorizado;
                </p>

                <p>
                    b. Ser informado por el Comité, como Responsables del Tratamiento o por el Encargado del
                    Tratamiento, previa solicitud, respecto del uso que le ha dado a sus datos personales;
                </p>

                <p>
                    c. Presentar ante la Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto
                    en la presente ley y las demás normas que la modifiquen, adicionen o complementen;
                </p>

                <p>
                    d. Revocar la autorización y/o solicitar la supresión del dato cuando en el Tratamiento no se
                    respeten los principios, derechos y garantías constitucionales y legales;
                </p>

                <p>
                    e. Acceder en forma gratuita a sus datos personales que hayan sido objeto de Tratamiento.
                </p>

                <p>
                    Dentro de esta política encontrará el procedimiento a través del cual el Comité garantiza el
                    ejercicio de todos sus derechos.
